    private void btnABRIRMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnABRIRMouseClicked
        // TODO add your handling code here:
        //Seleccionar JFileChooser, crear el objeto y seleccionar una ruta real en el sistema
        JFileChooser jfcAbrirArch = new JFileChooser("C:\\archivos");
        //Seleccionar jfcAbrirArch, show y escoger "Open Dialog"
        int resu = jfcAbrirArch.showOpenDialog(this);
        if(resu == JFileChooser.APPROVE_OPTION){
                BufferedReader br = null;
                InputStream is = null;
            try {
                System.out.println(jfcAbrirArch.getSelectedFile());
                //Necesitamos un reader para pasarselo al text area
                is = Files.newInputStream(jfcAbrirArch.getSelectedFile().toPath());
                InputStreamReader isr = new InputStreamReader(is);
                br = new BufferedReader(isr);
                txtareaEDITAR.read(br,"Abrir archivo");
            } catch (IOException ex) {
                Logger.getLogger(BlockDeNotas.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try{
                if(br!=null){
                br.close();
                is.close();
                  }
                }catch(IOException ex){
                    ex.printStackTrace();
                }        
            
        }
    }//GEN-LAST:event_btnABRIRMouseClicked
    }
    private void btnGUARDARMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGUARDARMouseClicked
        // Haremos lo mismo que con Abrir, pero con c√≥digo de salida
        JFileChooser jfcAbrirArch = new JFileChooser("C:\\archivos");
        int resu = jfcAbrirArch.showSaveDialog(this);
        if(resu == JFileChooser.APPROVE_OPTION){
            BufferedWriter bw = null;
            OutputStream os = null;
            
            try{
                os = Files.newOutputStream(jfcAbrirArch.getSelectedFile().toPath());
                OutputStreamWriter ow = new OutputStreamWriter(os);
                bw = new BufferedWriter(ow);
                txtareaEDITAR.write(ow);
            }catch(IOException ex){
                ex.printStackTrace();
            }finally{
            try{
                if(bw!=null){
                bw.close();
                os.close();
                  }
                }catch(IOException ex){
                    ex.printStackTrace();
                }
                
            }
        }
        
    }//GEN-LAST:event_btnGUARDARMouseClicked

